export function generate(input = {}) {
  const inp = normalizeInput(input);

  const mid = mustIntPositive("mid", inp.mid);
  const fc = mustPositive("fc", inp.fc);

  // optional RO/PR
  const roOv = readOptionalPositive(inp.ro);
  const ro = roOv != null ? roOv : DEFAULTS.ro_default;

  const prOv = readOptionalNumber(inp.pr);
  const pr = prOv != null ? mustInRange("pr", prOv, 0.0, 0.49) : DEFAULTS.pr_default;

  // optional E (auto from fcm=fc)
  const EOv = readOptionalPositive(inp.E);
  const E = EOv != null ? EOv : calculate_E_ec2_from_fcm(fc);

  // optional FT
  const ftOv = readOptionalPositive(inp.ft);
  const ft = ftOv != null ? ftOv : calculate_ft_ec2(fc);

  // strain-rate flag
  const strflgOv = readOptionalNumber(inp.strflg);
  const strflg = (strflgOv != null) ? (strflgOv >= 0.5 ? 1.0 : 0.0) : DEFAULTS.strflg_default;

  // HP depends on STRFLG per your notes
  const hp = (strflg >= 0.5) ? DEFAULTS.hp_rate : DEFAULTS.hp_qs;

  // FC0 only needed if STRFLG=1
  const fc0Ov = readOptionalPositive(inp.fc0_rate);
  const fc0 = (strflg >= 0.5)
    ? (fc0Ov != null ? fc0Ov : DEFAULTS.fc0_default_rate)
    : 0.0;

  // confinement preset
  const isCore = normalizeConfinement(inp.confinement) === "core";

  // overrides
  const eccOv = readOptionalPositive(inp.ecc_override);
  const wfOv  = readOptionalPositive(inp.wf_override);
  const efcOv = readOptionalPositive(inp.efc_override);

  const ecc = eccOv != null ? eccOv : computeEccentricity(fc, ft);
  const wf  = wfOv  != null ? wfOv  : computeWfFromPaper(fc, ft);
  const efc = efcOv != null ? efcOv : (isCore ? 0.010 : 0.005);

  // TYPE=1 bi-linear
  const wf1 = 0.15 * wf;
  const ft1 = 0.3 * ft;

  const qh0 = DEFAULTS.qh0;
  const ah = DEFAULTS.ah;
  const bh = DEFAULTS.bh;
  const ch = DEFAULTS.ch;
  const dh = DEFAULTS.dh;
  const as = DEFAULTS.as;
  const df = DEFAULTS.df;

  const type = DEFAULTS.type;
  const bs = DEFAULTS.bs;
  const failflg = DEFAULTS.failflg;

  const tag = isCore ? "CORE(confined)" : "COVER(unconfined)";
  const rateTag = (strflg >= 0.5) ? "rate=on" : "rate=off";
  const titleLine = `MAT_273 CDP fc=${toFixed(fc, 1)}MPa, ft=${toFixed(ft, 3)}MPa, ${tag}, E=${EOv != null ? "manual" : "auto(EC2)"}, ${rateTag}`;

  const lines = [];
  lines.push("$# LS-DYNA Keyword file created by LS-PrePost");
  lines.push("*KEYWORD");
  MATERIAL_CARDS_BANNER.forEach(s => lines.push(s));

  lines.push("*MAT_CONCRETE_DAMAGE_PLASTIC_MODEL_TITLE");
  lines.push(titleLine);

  lines.push(UNITS_LINE);
  lines.push(MODEL_LINE);
  lines.push(HUB_LINE);

  lines.push("$#     MID        RO         E        PR       ECC       QH0        FT        FC");
  lines.push(
    `${fmt10(mid)}` +
    `${fmt10(ro, 7)}` +
    `${fmt10(E, 1)}` +
    `${fmt10(pr, 2)}` +
    `${fmt10(ecc, 6)}` +
    `${fmt10(qh0, 3)}` +
    `${fmt10(ft, 6)}` +
    `${fmt10(fc, 6)}`
  );

  lines.push("$#      HP        AH        BH        CH        DH        AS        DF       FC0");
  lines.push(
    `${fmt10(hp, 6)}` +
    `${fmt10(ah, 6)}` +
    `${fmt10(bh, 6)}` +
    `${fmt10(ch, 6)}` +
    `${fmt10(dh, 6)}` +
    `${fmt10(as, 6)}` +
    `${fmt10(df, 6)}` +
    `${fmt10(fc0, 6)}`
  );

  lines.push("$#    TYPE        BS        WF       WF1       FT1    STRFLG   FAILFLG       EFC");
  lines.push(
    `${fmt10(type, 6)}` +
    `${fmt10(bs, 6)}` +
    `${fmt10(wf, 6)}` +
    `${fmt10(wf1, 6)}` +
    `${fmt10(ft1, 6)}` +
    `${fmt10(strflg, 1)}` +
    `${fmt10(failflg, 6)}` +
    `${fmt10(efc, 6)}`
  );

  lines.push("*END");
  lines.push("");

  const filename = `MAT_273_CDP_fc=${toFixed(fc, 1)}MPa_${isCore ? "core" : "cover"}_mid=${mid}.k`;
  return { filename, keyword: lines.join("\n") };
}

// --- Auto FT (your provided function)
function calculate_ft_ec2(fc) {
  if (fc <= 50) return 0.3 * Math.pow(fc, 2 / 3);
  return 2.12 * Math.log(1 + 0.1 * (fc + 8));
}

// --- Auto E (EC2 using fcm directly)
function calculate_E_ec2_from_fcm(fcm) {
  return 22000.0 * Math.pow(fcm / 10.0, 0.3); // MPa
}

// --- ECC / WF workflow formulas
function computeEccentricity(fc, ft) {
  const fbc = 1.16 * fc;
  const denom = fbc * (fc * fc - ft * ft);
  if (Math.abs(denom) < 1e-12) throw new Error("ECC denominator ~0; check fc/ft values.");
  return (ft * (fbc * fbc - ft * ft)) / denom;
}
function computeGfFromPaper(fc) {
  return 73.0 * Math.pow(fc, 0.18);
}
function computeWfFromPaper(fc, ft) {
  const Gf = computeGfFromPaper(fc);
  return (4.444 * Gf) / ft;
}

// --- helpers
function normalizeConfinement(x) {
  const v = (x ?? "").toString().trim().toLowerCase();
  if (v === "core" || v === "confined") return "core";
  return "cover";
}
function normalizeInput(obj) {
  return { ...obj };
}

// Optional numeric: blank/null/undefined/0/"0" => null
function readOptionalNumber(x) {
  if (x === undefined || x === null) return null;
  const s = String(x).trim();
  if (s === "" || s === "0") return null;
  const v = Number(s);
  if (!Number.isFinite(v)) return null;
  if (v === 0) return null;
  return v;
}
// Optional positive: only accept >0, else null
function readOptionalPositive(x) {
  const v = readOptionalNumber(x);
  if (v == null) return null;
  if (v <= 0) return null;
  return v;
}

function mustPositive(name, x) {
  const v = Number(x);
  if (!Number.isFinite(v)) throw new Error(`${name} must be a number`);
  if (v <= 0) throw new Error(`${name} must be > 0`);
  return v;
}
function mustIntPositive(name, x) {
  const v = Number(x);
  if (!Number.isFinite(v)) throw new Error(`${name} must be a number`);
  const vi = Math.trunc(v);
  if (vi <= 0) throw new Error(`${name} must be an integer > 0`);
  return vi;
}
function mustInRange(name, x, lo, hi) {
  const v = Number(x);
  if (!Number.isFinite(v)) throw new Error(`${name} must be a number`);
  if (v < lo || v > hi) throw new Error(`${name} must be in [${lo}, ${hi}]`);
  return v;
}
function toFixed(x, n) {
  const v = Number(x);
  if (!Number.isFinite(v)) return String(x);
  return v.toFixed(n);
}
function fmt10(x, decimals = 6) {
  if (Number.isInteger(x)) return String(x).padStart(10, " ");
  const v = Number(x);
  if (!Number.isFinite(v)) return String(x).padStart(10, " ");
  const av = Math.abs(v);
  if (av !== 0 && (av >= 1e6 || av < 1e-4)) {
    return v.toExponential(3).replace("e", "E").padStart(10, " ");
  }
  return v.toFixed(decimals).padStart(10, " ");
}
